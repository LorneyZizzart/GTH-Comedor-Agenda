package Controlador;

import Entidad.Plan_accion_files;
import Modelo.Plan_accion_filesModel;
import java.util.List;

public class Plan_accion_filesController {

    public String SaveFile(Plan_accion_files file) {
        Plan_accion_filesModel mo = new Plan_accion_filesModel();
        return mo.SaveFile(file);
    }

    public List<Plan_accion_files> GetAllFileByPlanacionid(int id) {
        Plan_accion_filesModel mo = new Plan_accion_filesModel();
        return mo.GetAllFileByPlanacionid(id);
    }
    
    public String DeleteFile(Plan_accion_files file) {
        Plan_accion_filesModel mo = new Plan_accion_filesModel();
        return mo.DeleteFile(file);
    }
}
