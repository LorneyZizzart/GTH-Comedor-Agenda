package Entidad;

public class Cuestionario_respuestas {

    private int Respuesta_id;
    private int Cuestionario_evalua_id;
    private int Pregunta_id;
    private int Nota;

    public int getRespuesta_id() {
        return Respuesta_id;
    }

    public void setRespuesta_id(int Respuesta_id) {
        this.Respuesta_id = Respuesta_id;
    }

    public int getCuestionario_evalua_id() {
        return Cuestionario_evalua_id;
    }

    public void setCuestionario_evalua_id(int Cuestionario_evalua_id) {
        this.Cuestionario_evalua_id = Cuestionario_evalua_id;
    }

    public int getPregunta_id() {
        return Pregunta_id;
    }

    public void setPregunta_id(int Pregunta_id) {
        this.Pregunta_id = Pregunta_id;
    }

    public int getNota() {
        return Nota;
    }

    public void setNota(int Nota) {
        this.Nota = Nota;
    }
    
    
}
