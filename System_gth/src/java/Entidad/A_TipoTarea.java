public class A_TipoTarea extends A_RepeticionTarea {
    private int IdTipoTarea;
    private String NombreTarea;
    private int EstadoTipoTarea;
    
    public void setEstadoTipoTarea(int estadoTipoTarea){
        this.EstadoTipoTarea = estadoTipoTarea;
    }
    
    public int getEstadoTipoTarea(){
        return EstadoTipoTarea;
    }
    
    public void setNombreTarea(String nombreTarea){
        this.NombreTarea = nombreTarea;
    }
    
    public String getNombreTarea(){
        return NombreTarea;
    }
    
    public void setIdTipoTarea(int idTipoTarea){
        this.IdTipoTarea = idTipoTarea;
    }
    
    public int getIdTipoTarea(){
        return IdTipoTarea;
    }
}
