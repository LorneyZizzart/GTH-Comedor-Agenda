package Entidad;

import java.util.Date;

public class Sector_Cuestionario {

    private int SectorEncuesta_id;
    private int Cuestionario_id;
    private int Area_id;
    private String Sector;
    private String Sector_nombre;
    private Date Fecha_creacion;

    public String getSector_nombre() {
        return Sector_nombre;
    }

    public void setSector_nombre(String Sector_nombre) {
        this.Sector_nombre = Sector_nombre;
    }
    
    public int getSectorEncuesta_id() {
        return SectorEncuesta_id;
    }

    public void setSectorEncuesta_id(int SectorEncuesta_id) {
        this.SectorEncuesta_id = SectorEncuesta_id;
    }

    public int getCuestionario_id() {
        return Cuestionario_id;
    }

    public void setCuestionario_id(int Cuestionario_id) {
        this.Cuestionario_id = Cuestionario_id;
    }

    public int getArea_id() {
        return Area_id;
    }

    public void setArea_id(int Area_id) {
        this.Area_id = Area_id;
    }

    public String getSector() {
        return Sector;
    }

    public void setSector(String Sector) {
        this.Sector = Sector;
    }

    public Date getFecha_creacion() {
        return Fecha_creacion;
    }

    public void setFecha_creacion(Date Fecha_creacion) {
        this.Fecha_creacion = Fecha_creacion;
    }

}
